package com.example.quiztest2.json;

public class Image {
    private String full;
    private String sprite;
    private String group;
    private Short x;
    private Short y;
    private Short w;
    private Short h;

    public String getFull() {
        return full;
    }

    public void setFull(String full) {
        this.full = full;
    }

    public String getSprite() {
        return sprite;
    }

    public void setSprite(String sprite) {
        this.sprite = sprite;
    }

    public String getGroup() {
        return group;
    }

    public void setGroup(String group) {
        this.group = group;
    }

    public Short getX() {
        return x;
    }

    public void setX(Short x) {
        this.x = x;
    }

    public Short getY() {
        return y;
    }

    public void setY(Short y) {
        this.y = y;
    }

    public Short getW() {
        return w;
    }

    public void setW(Short w) {
        this.w = w;
    }

    public Short getH() {
        return h;
    }

    public void setH(Short h) {
        this.h = h;
    }
}
